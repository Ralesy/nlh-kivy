def apply_random_defeat(player) -> int:
    """Применить случайное событие поражения к игроку.

    Returns:
        int: индекс применённого события (0–7) для отображения текста в таверне.
    """
    idx = random.randint(0, len(DEFEAT_EVENTS) - 1)
    event = DEFEAT_EVENTS[idx]
    try:
        event["apply"](player)
    except Exception:
        pass
    return idx