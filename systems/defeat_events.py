"""
defeat_events — система событий поражения при смерти игрока в real-time бою.

При гибели персонажа случайно выбирается одно из 8 событий,
каждое со своим текстом и набором потерь (деньги, броня, оружие, зелья,
случайный предмет). После применения событие сохраняется в app._defeat_event
для отображения нарративного текста при входе в таверну.
"""

import random


# ──────────────────────────────────────────────────────────────────────────
# Структура события: (текст, apply_fn(player))
# ──────────────────────────────────────────────────────────────────────────

def _event_1(player):
    """15% денег остаётся, 85% пропадает."""
    coins = getattr(player, "coins", 0)
    kept = int(coins * 0.15)
    player.coins = kept


def _event_2(player):
    """Всё снаряжение на месте, денег 0."""
    player.coins = 0


def _event_3(player):
    """Полностью без снаряжения, деньги целы."""
    inv = getattr(player, "inventory", None)
    if inv:
        inv.items.clear()
    player.weapon = None
    player.armor = None
    if hasattr(player, "notify_listeners"):
        player.notify_listeners("stats_changed")


def _event_4(player):
    """Из инвентаря пропадают все зелья лечения."""
    inv = getattr(player, "inventory", None)
    if not inv:
        return
    to_remove = []
    for item_id, qty in list(inv.items.items()):
        from data.items import ItemDatabase
        template = ItemDatabase.get(item_id)
        if template and hasattr(template, "item_type") and template.item_type == "potion":
            to_remove.append(item_id)
    for rid in to_remove:
        inv.items.pop(rid, None)


def _event_5(player):
    """Пропадает один случайный предмет."""
    inv = getattr(player, "inventory", None)
    if not inv:
        return
    item_ids = list(inv.items.keys())
    if item_ids:
        victim = random.choice(item_ids)
        inv.items.pop(victim, None)


def _event_6(player):
    """Пропадает всё оружие."""
    inv = getattr(player, "inventory", None)
    if inv:
        to_remove = []
        for item_id, qty in list(inv.items.items()):
            from data.items import ItemDatabase
            template = ItemDatabase.get(item_id)
            if template and hasattr(template, "item_type") and template.item_type == "weapon":
                to_remove.append(item_id)
        for rid in to_remove:
            inv.items.pop(rid, None)
    player.weapon = None
    if hasattr(player, "notify_listeners"):
        player.notify_listeners("stats_changed")


def _event_7(player):
    """Пропадает вся броня."""
